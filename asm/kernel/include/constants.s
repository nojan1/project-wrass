LCD_DDRB = LCD_PORTA + 1
LCD_DDRA = LCD_DDRB + 1

IO_BASE = $A000
IO_VIA1_PORTB = IO_BASE + 0
IO_VIA1_PORTA = IO_VIA1_PORTB +1
IO_VIA1_DDRB = IO_VIA1_PORTA + 1
IO_VIA1_DDRA = IO_VIA1_DDRB + 1

